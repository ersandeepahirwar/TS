// Arithmetic Operators

console.log(10 + "5");
console.log(10 + 5);
console.log(10 - 5);
console.log(10 * 5);
console.log(10 / 5);
console.log(10 % 5);
console.log(10 ** 5);

// Relational Operators

console.log(10 < 15);
console.log("10" < "15");

// Logical Operators

console.log(1 && 5);
console.log(0 && 5);
console.log(true && 5);
console.log(false && 5);
